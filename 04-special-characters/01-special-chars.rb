# Let's print some text
text = "Hello people"
puts text

# Something a bit more complex
text = "Hello people \n"
puts text

# A little bit more
text = "\tHello\tpeople\nof the world"
puts text

# Just a little bit more
text = "\n\n\tHello\tpeople\n\tof the world"
puts text

