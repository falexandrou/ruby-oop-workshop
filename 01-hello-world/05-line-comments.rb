# We have line comments and inline comments
# The execution will stop right before the '#' symbol for that line

puts "It would be nice to say hello back" # Oh, hi, this is an inline comment
# Output: It would be nice to say hello back without the text for the inline comment

puts "There is no problem printing '#' anywhere else though"
# Output: There is no problem printing '#' anywhere else though

